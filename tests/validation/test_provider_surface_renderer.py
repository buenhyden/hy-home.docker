    def test_empty_owned_root_identity_race_restores_unknown_replacement(self) -> None:
        renderer = load_renderer()
        with tempfile.TemporaryDirectory() as directory:
            root = pathlib.Path(directory)
            target = root / ".codex/skills"
            target.mkdir(parents=True)
            real_rename = renderer.os.rename
            injected = False

            def replace_before_capture(src, dst, *, src_dir_fd=None, dst_dir_fd=None):
                nonlocal injected
                if not injected and src == "skills":
                    injected = True
                    real_rename(
                        src,
                        ".skills.original-race",
                        src_dir_fd=src_dir_fd,
                        dst_dir_fd=dst_dir_fd,
                    )
                    renderer.os.mkdir(src, 0o755, dir_fd=src_dir_fd)
                    replacement_fd = renderer.os.open(
                        src,
                        renderer.os.O_RDONLY | renderer.os.O_DIRECTORY,
                        dir_fd=src_dir_fd,
                    )
                    try:
                        fd = renderer.os.open(
                            "unknown.txt",
                            renderer.os.O_WRONLY
                            | renderer.os.O_CREAT
                            | renderer.os.O_EXCL,
                            0o644,
                            dir_fd=replacement_fd,
                        )
                        try:
                            renderer.os.write(fd, b"concurrent user content\n")
                        finally:
                            renderer.os.close(fd)
                    finally:
                        renderer.os.close(replacement_fd)
                return real_rename(
                    src,
                    dst,
                    src_dir_fd=src_dir_fd,
                    dst_dir_fd=dst_dir_fd,
                )

            with mock.patch.object(
                renderer.os, "rename", side_effect=replace_before_capture
            ):
                with self.assertRaises(ValueError):
                    renderer._remove_empty_owned_root(
                        root, pathlib.Path(".codex/skills")
                    )

            self.assertEqual(
                b"concurrent user content\n",
                (target / "unknown.txt").read_bytes(),
            )
            self.assertEqual([], list((root / ".codex").glob(".skills.quarantine-*")))

