MARKER = "Generated by scripts/operations/provider_surface_renderer.py"


def load_renderer():
    spec = importlib.util.spec_from_file_location("provider_surface_renderer", RENDERER)
    if spec is None or spec.loader is None:
        raise RuntimeError(f"unable to load renderer: {RENDERER}")
    module = importlib.util.module_from_spec(spec)
    sys.modules[spec.name] = module
    spec.loader.exec_module(module)
    return module


def copy_stage00(root: pathlib.Path) -> None:
    shutil.copytree(
        ROOT / "docs/00.agent-governance",
        root / "docs/00.agent-governance",
        dirs_exist_ok=True,
    )
    spec_source = (
        ROOT / "docs/03.specs/132-agent-governance-harness-convergence/spec.md"
    )
    spec_target = root / spec_source.relative_to(ROOT)
    spec_target.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(spec_source, spec_target)


def generated_surface(function_id: str = "retired") -> str:
    return (
        "---\n"
        f"name: {function_id}\n"
        f"description: Canonical common function for {function_id}; "
        "owned by eval-engineer.\n"
        "---\n\n"
        f"<!-- {MARKER}; source: docs/00.agent-governance/agents/functions/"
        f"{function_id}.md -->\n\n"
        "# Generated surface\n"
    )


