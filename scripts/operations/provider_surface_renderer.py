def _frontmatter_free_body(text: str) -> str:
    if not text.startswith("---\n"):
        return text.strip() + "\n"
    boundary = text.find("\n---\n", 4)
    if boundary < 0:
        raise ValueError("canonical source has unterminated frontmatter")
    return text[boundary + 5 :].strip() + "\n"


def _rebase_link_destination(
    destination: str,
    source_path: pathlib.PurePosixPath,
    output_path: pathlib.PurePosixPath,
) -> str:
    bracketed = destination.startswith("<") and destination.endswith(">")
    raw = destination[1:-1] if bracketed else destination
    parsed = urllib.parse.urlsplit(raw)
    if (
        not parsed.path
        or raw.startswith(("#", "/", "//"))
        or parsed.scheme
        or parsed.netloc
    ):
        return destination
    canonical_target = posixpath.normpath(
        posixpath.join(source_path.parent.as_posix(), parsed.path)
    )
    if canonical_target == ".." or canonical_target.startswith("../"):
        raise ValueError("canonical Markdown link escapes the repository")
    rebased_path = posixpath.relpath(canonical_target, output_path.parent.as_posix())
    rebased = urllib.parse.urlunsplit(
        ("", "", rebased_path, parsed.query, parsed.fragment)
    )
    return f"<{rebased}>" if bracketed else rebased


def _rebase_markdown_links(
    body: str,
    source_path: pathlib.PurePosixPath,
    output_path: pathlib.PurePosixPath,
) -> str:
    def replace_inline(match: re.Match[str]) -> str:
        destination = _rebase_link_destination(
            match.group("destination"), source_path, output_path
        )
        return (
            f"{match.group('prefix')}{destination}"
            f"{match.group('title') or ''}{match.group('suffix')}"
        )

    def replace_reference(match: re.Match[str]) -> str:
        destination = _rebase_link_destination(
            match.group("destination"), source_path, output_path
        )
        return f"{match.group('prefix')}{destination}"

    return REFERENCE_LINK_PATTERN.sub(
        replace_reference, INLINE_LINK_PATTERN.sub(replace_inline, body)
    )


def _has_exact_generated_marker(content: bytes) -> bool:
    return GENERATED_SURFACE_PREFIX_PATTERN.match(content) is not None


def _has_native_generated_marker(content: bytes) -> bool:
    marker = GENERATED_MARKER.encode()
    lines = content.splitlines()
    if len(lines) >= 2 and lines[0] == b"#!/usr/bin/env bash":
        return lines[1].startswith(b"# " + marker + b";")
    if content.startswith(b"---\n"):
        boundary = content.find(b"\n---\n", 4)
        if boundary < 0:
            return False
        line = content[boundary + 5 :].lstrip().splitlines()[:1]
        return bool(
            line
            and line[0].startswith(b"<!-- " + marker + b"; source: ")
            and line[0].endswith(b" -->")
        )
    first_line = lines[:1]
    return bool(first_line and first_line[0].startswith(b"# " + marker + b"; source: "))


def load_catalog(root: pathlib.Path) -> Catalog:
    root = root.absolute()
    bundle = load_contract_bundle(root)
    findings = validate_contract_bundle(root, bundle)
    if findings:
        raise ValueError("canonical Stage 00 contract is invalid")

    agents = tuple(
        AgentRecord(
            agent_id=str(entry["agent_id"]),
            scope=str(entry["scope"]),
            tier=str(entry["tier"]),
            category=str(entry["category"]),
            catalog_path=str(entry["catalog_path"]),
            permission_profile=str(entry["permission_profile"]),
            work_profile=str(entry["work_profile"]),
            function_ids=tuple(str(item) for item in entry["function_ids"]),
            source_text=read_repository_text(root, str(entry["catalog_path"])),
        )
        for entry in sorted(bundle.catalog["agents"], key=lambda item: item["agent_id"])
    )
    functions = tuple(
        FunctionRecord(
            function_id=str(entry["function_id"]),
            scope=str(entry["scope"]),
            catalog_path=str(entry["catalog_path"]),
            owner_agent=str(entry["owner_agent"]),
            inputs=tuple(str(item) for item in entry["inputs"]),
            outputs=tuple(str(item) for item in entry["outputs"]),
            gates=tuple(str(item) for item in entry["gates"]),
            source_text=read_repository_text(root, str(entry["catalog_path"])),
        )
        for entry in sorted(
            bundle.catalog["functions"], key=lambda item: item["function_id"]
        )
    )
    return Catalog(agents=agents, functions=functions)


def render_agent(provider: str, agent: AgentRecord) -> bytes:
    if provider not in {"agents-md", "claude", "codex", "gemini"}:
        raise ValueError(f"unsupported provider: {provider}")
    source = agent.catalog_path
    if provider == "codex":
        return (
            f'name = "{agent.agent_id}"\n'
            f'layer = "{agent.scope}"\n'
            f'source_catalog = "{source}"\n'
        ).encode()
    return (
        "---\n"
        f"name: {agent.agent_id}\n"
        f"description: Canonical {agent.scope} role projection for {agent.agent_id}.\n"
        "---\n\n"
        f"<!-- {GENERATED_MARKER}; source: {source} -->\n\n"
        f"@{source}\n"
    ).encode()


def render_function(provider: str, function: FunctionRecord) -> bytes:
    if provider not in {"agents-md", "claude", "gemini"}:
        raise ValueError(f"unsupported provider: {provider}")
    output_root = {
        "agents-md": pathlib.PurePosixPath(".agents/skills"),
        "claude": pathlib.PurePosixPath(".claude/skills"),
        "gemini": pathlib.PurePosixPath(".gemini/skills"),
    }[provider]
    output_path = output_root / function.function_id / "SKILL.md"
    source_path = pathlib.PurePosixPath(function.catalog_path)
    body = _rebase_markdown_links(
        _frontmatter_free_body(function.source_text), source_path, output_path
    )
    description = (
        f"Canonical {function.scope} function for {function.function_id}; "
        f"owned by {function.owner_agent}."
    )
    return (
        "---\n"
        f"name: {function.function_id}\n"
        f"description: {description}\n"
        "---\n\n"
        f"<!-- {GENERATED_MARKER}; source: {function.catalog_path} -->\n\n"
        f"{body}"
    ).encode()


def _provider_defaults(root: pathlib.Path) -> dict[tuple[str, str], tuple[str, str]]:
    bundle = load_contract_bundle(root.absolute())
    defaults: dict[tuple[str, str], tuple[str, str]] = {}
    for profile in bundle.providers["work_profiles"]:
        profile_id = str(profile["profile_id"])
        for entry in profile["defaults"]:
            control = entry.get("reasoning")
            if control is None:
                control = entry.get("effort") or entry.get("thinking")
            defaults[(profile_id, str(entry["provider"]))] = (
                str(entry["model_id"]),
                str(control),
            )
    return defaults


def _agent_description(agent: AgentRecord) -> str:
    category = agent.category.replace("-", " ")
    return (
        f"Canonical {agent.scope} {category} role for {agent.agent_id}; "
        "use for its Stage 00 catalog responsibilities."
    )


def _agent_body(agent: AgentRecord, output_path: pathlib.PurePosixPath) -> str:
    source_path = pathlib.PurePosixPath(agent.catalog_path)
    return _rebase_markdown_links(
        _frontmatter_free_body(agent.source_text), source_path, output_path
    )


def _markdown_projection(
    metadata: Mapping[str, object], body: str, source: str
) -> bytes:
    frontmatter = yaml.safe_dump(
        dict(metadata),
        allow_unicode=True,
        default_flow_style=False,
        sort_keys=False,
        width=1000,
    ).strip()
    return (
        f"---\n{frontmatter}\n---\n\n"
        f"<!-- {GENERATED_MARKER}; source: {source} -->\n\n"
        f"{body.rstrip()}\n"
    ).encode()


def _render_claude_agent(
    agent: AgentRecord, defaults: Mapping[tuple[str, str], tuple[str, str]]
) -> bytes:
    model, _reasoning = defaults[(agent.work_profile, "claude")]
    read_only = agent.permission_profile == "read-only"
    tools = ["Read", "Grep", "Glob"]
    if agent.tier == "supervisor":
        tools.append("Agent")
    elif not read_only:
        tools.extend(("Edit", "Write", "Bash"))
    output = pathlib.PurePosixPath(f".claude/agents/{agent.agent_id}.md")
    return _markdown_projection(
        {
            "name": agent.agent_id,
            "description": _agent_description(agent),
            "tools": tools,
            "model": model,
            "permissionMode": "plan" if read_only else "default",
            "skills": list(agent.function_ids),
        },
        _agent_body(agent, output),
        agent.catalog_path,
    )


def _render_codex_agent(
    agent: AgentRecord, defaults: Mapping[tuple[str, str], tuple[str, str]]
) -> bytes:
    model, reasoning = defaults[(agent.work_profile, "codex")]
    output = pathlib.PurePosixPath(f".codex/agents/{agent.agent_id}.toml")
    body = (
        f"{GENERATED_MARKER}; source: {agent.catalog_path}\n\n"
        f"{_agent_body(agent, output).rstrip()}\n"
    )
    fields = {
        "name": agent.agent_id,
        "description": _agent_description(agent),
        "developer_instructions": body,
        "model": model,
        "model_reasoning_effort": reasoning,
        "sandbox_mode": (
            "read-only"
            if agent.permission_profile == "read-only"
            else "workspace-write"
        ),
    }
    return (
        f"# {GENERATED_MARKER}; source: {agent.catalog_path}\n"
        + "".join(f"{key} = {json.dumps(value)}\n" for key, value in fields.items())
    ).encode()


