GENERATED_MARKER = "Generated by scripts/operations/provider_surface_renderer.py"
GENERATED_SURFACE_PREFIX_PATTERN = re.compile(
    rb"\A---\n"
    rb"name: (?P<function_id>[a-z0-9]+(?:-[a-z0-9]+)*)\n"
    rb"description: Canonical [a-z0-9-]+ function for (?P=function_id); "
    rb"owned by [a-z0-9]+(?:-[a-z0-9]+)*\.\n"
    rb"---\n\n"
    rb"<!-- Generated by scripts/operations/provider_surface_renderer\.py; "
    rb"source: docs/00\.agent-governance/agents/functions/"
    rb"(?P=function_id)\.md -->\n\n"
)
INLINE_LINK_PATTERN = re.compile(
    r"(?P<prefix>!?\[[^\]\n]*\]\()"
    r"(?P<destination><[^>\n]+>|[^\s)]+)"
    r"(?P<title>\s+(?:\"[^\"\n]*\"|'[^'\n]*'|\([^()\n]*\)))?"
    r"(?P<suffix>\))"
)
REFERENCE_LINK_PATTERN = re.compile(
    r"(?m)^(?P<prefix>[ \t]{0,3}\[[^\]\n]+\]:[ \t]*)"
    r"(?P<destination><[^>\n]+>|\S+)"
)
