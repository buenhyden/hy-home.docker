def find_native_inventory_drift(
    root: pathlib.Path, expected: Mapping[pathlib.Path, bytes]
) -> list[Finding]:
    """Return exact, non-following inventory drift for renderer-owned trees."""

    expected_paths = set(expected)
    findings: set[Finding] = set()
    for managed_root in NATIVE_MANAGED_ROOTS:
        absolute = root / managed_root
        if not absolute.exists() and not absolute.is_symlink():
            continue
        try:
            _assert_confined_directory(root, managed_root)
        except (OSError, ValueError):
            findings.add(Finding(managed_root, "unsafe"))
            continue

        allowed_directories = {managed_root}
        for path in expected_paths:
            if not path.is_relative_to(managed_root):
                continue
            parent = path.parent
            while parent.is_relative_to(managed_root):
                allowed_directories.add(parent)
                if parent == managed_root:
                    break
                parent = parent.parent

        stack = [managed_root]
        while stack:
            directory = stack.pop()
            try:
                entries = sorted(
                    os.scandir(root / directory), key=lambda entry: entry.name
                )
            except OSError:
                findings.add(Finding(directory, "unsafe"))
                continue
            if not entries and directory not in allowed_directories:
                findings.add(Finding(directory, "unowned"))
            for entry in entries:
                relative = directory / entry.name
                try:
                    mode = entry.stat(follow_symlinks=False).st_mode
                except OSError:
                    findings.add(Finding(relative, "unsafe"))
                    continue
                if stat.S_ISDIR(mode):
                    stack.append(relative)
                    continue
                if relative in expected_paths:
                    if not stat.S_ISREG(mode):
                        findings.add(Finding(relative, "unsafe"))
                    continue
                if _is_legacy_migration_path(relative):
                    findings.add(Finding(relative, "stale"))
                    continue
                if not stat.S_ISREG(mode):
                    findings.add(Finding(relative, "unsafe"))
                    continue
                try:
                    content = read_repository_text(root, relative.as_posix()).encode()
                except ContractLoadError:
                    findings.add(Finding(relative, "unsafe"))
                    continue
                marker_owned = _has_exact_generated_marker(
                    content
                ) or _has_native_generated_marker(content)
                findings.add(Finding(relative, "stale" if marker_owned else "unowned"))

        if managed_root == pathlib.Path(".codex/skills") and not findings.intersection(
            {
                finding
                for finding in findings
                if finding.path.is_relative_to(managed_root)
            }
        ):
            findings.add(Finding(managed_root, "stale"))
    return sorted(findings)


def find_native_projection_drift(root: pathlib.Path) -> list[Finding]:
    expected = expected_native_projection(root)
    findings: list[Finding] = []
    for path, content in expected.items():
        try:
            current = read_repository_text(root, path.as_posix()).encode()
        except ContractLoadError as error:
            kind = (
                "missing" if error.code == "AGC-REPOSITORY-FILE-MISSING" else "unsafe"
            )
            findings.append(Finding(path, kind))
            continue
        if current != content:
            findings.append(Finding(path, "content"))
            continue
        desired_mode = 0o755 if path in NATIVE_EXECUTABLES else 0o644
        try:
            actual_mode = (root / path).lstat().st_mode
        except OSError:
            findings.append(Finding(path, "unsafe"))
            continue
        if not stat.S_ISREG(actual_mode) or stat.S_IMODE(actual_mode) != desired_mode:
            findings.append(Finding(path, "mode"))
    findings.extend(find_native_inventory_drift(root, expected))
    return sorted(set(findings))


def _assert_confined_directory(root: pathlib.Path, relative: pathlib.Path) -> None:
    current = root
    for part in relative.parts:
        current = current / part
        if not current.exists() and not current.is_symlink():
            return
        mode = current.lstat().st_mode
        if stat.S_ISLNK(mode) or not stat.S_ISDIR(mode):
            raise ValueError("managed projection directory is unsafe")


def find_projection_drift(root: pathlib.Path, provider: str) -> list[Finding]:
    if provider == "codex":
        owned_root = _owned_root(provider)
        absolute = root / owned_root
        if not absolute.exists() and not absolute.is_symlink():
            return []
        try:
            _assert_confined_directory(root, owned_root)
        except (OSError, ValueError):
            return [Finding(owned_root, "unsafe")]
        return [Finding(owned_root, "stale")]
    expected = expected_projection(root, provider)
    findings: list[Finding] = []
    for path, content in expected.items():
        try:
            current = read_repository_text(root, path.as_posix()).encode()
        except ContractLoadError as error:
            kind = (
                "missing" if error.code == "AGC-REPOSITORY-FILE-MISSING" else "unsafe"
            )
            findings.append(Finding(path, kind))
            continue
        if current != content:
            findings.append(Finding(path, "content"))
    owned_expected = {
        path: content
        for path, content in expected.items()
        if path.is_relative_to(_owned_root(provider))
    }
    for path in sorted(find_managed_stale_files(root, owned_expected)):
        if path.is_relative_to(_owned_root(provider)):
            findings.append(Finding(path, "stale"))
    return sorted(set(findings))


@contextlib.contextmanager
def _open_confined_directory(
    root: pathlib.Path, relative: pathlib.Path, *, create: bool
):
    directory_flags = os.O_RDONLY | os.O_DIRECTORY | os.O_NOFOLLOW
    directory_flags |= getattr(os, "O_CLOEXEC", 0)
    descriptors: list[int] = []
    try:
        current = os.open(root, directory_flags)
        descriptors.append(current)
        for part in relative.parts:
            try:
                child = os.open(part, directory_flags, dir_fd=current)
            except FileNotFoundError:
                if not create:
                    raise
                os.mkdir(part, 0o755, dir_fd=current)
                child = os.open(part, directory_flags, dir_fd=current)
            descriptors.append(child)
            current = child
        yield current
    finally:
        for descriptor in reversed(descriptors):
            try:
                os.close(descriptor)
            except OSError:
                pass


def _read_regular_fd(parent_fd: int, name: str) -> bytes | None:
    flags = os.O_RDONLY | os.O_NONBLOCK | os.O_NOFOLLOW
    flags |= getattr(os, "O_CLOEXEC", 0)
    try:
        descriptor = os.open(name, flags, dir_fd=parent_fd)
    except FileNotFoundError:
        return None
    try:
        if not stat.S_ISREG(os.fstat(descriptor).st_mode):
            raise ValueError("managed projection output is not a regular file")
        chunks: list[bytes] = []
        while True:
            chunk = os.read(descriptor, 64 * 1024)
            if not chunk:
                return b"".join(chunks)
            chunks.append(chunk)
    finally:
        os.close(descriptor)


def _normalize_existing_regular(
    parent_fd: int, name: str, content: bytes, mode: int
) -> bool:
    flags = os.O_RDWR | os.O_NONBLOCK | os.O_NOFOLLOW
    flags |= getattr(os, "O_CLOEXEC", 0)
    try:
        descriptor = os.open(name, flags, dir_fd=parent_fd)
    except FileNotFoundError:
        return False
    try:
        if not stat.S_ISREG(os.fstat(descriptor).st_mode):
            raise ValueError("managed projection output is not a regular file")
        chunks: list[bytes] = []
        while True:
            chunk = os.read(descriptor, 64 * 1024)
            if not chunk:
                break
            chunks.append(chunk)
        if b"".join(chunks) != content:
            return False
        os.fchmod(descriptor, mode)
        return True
    finally:
        os.close(descriptor)


def _write_confined(
    root: pathlib.Path,
    relative: pathlib.Path,
    content: bytes,
    *,
    mode: int = 0o644,
) -> None:
    with _open_confined_directory(root, relative.parent, create=True) as parent_fd:
        if _normalize_existing_regular(parent_fd, relative.name, content, mode):
            return
        temporary = f".{relative.name}.tmp-{secrets.token_hex(12)}"
        flags = os.O_WRONLY | os.O_CREAT | os.O_EXCL | os.O_NOFOLLOW
        flags |= getattr(os, "O_CLOEXEC", 0)
        descriptor = os.open(temporary, flags, 0o644, dir_fd=parent_fd)
        try:
            try:
                view = memoryview(content)
                while view:
                    written = os.write(descriptor, view)
                    if written <= 0:
                        raise OSError("managed projection write made no progress")
                    view = view[written:]
                os.fchmod(descriptor, mode)
                os.fsync(descriptor)
            finally:
                os.close(descriptor)
                descriptor = -1
            os.replace(
                temporary,
                relative.name,
                src_dir_fd=parent_fd,
                dst_dir_fd=parent_fd,
            )
        except BaseException:
            if descriptor >= 0:
                try:
                    os.close(descriptor)
                except OSError:
                    pass
            try:
                os.unlink(temporary, dir_fd=parent_fd)
            except OSError:
                pass
            raise


def _restore_quarantine(parent_fd: int, quarantine: str, original: str) -> None:
    try:
        os.link(
            quarantine,
            original,
            src_dir_fd=parent_fd,
            dst_dir_fd=parent_fd,
            follow_symlinks=False,
        )
    except OSError as error:
        raise ValueError(
            "managed stale output could not be restored without overwrite"
        ) from error
    try:
        os.unlink(quarantine, dir_fd=parent_fd)
    except OSError as error:
        raise ValueError("managed stale output quarantine cleanup failed") from error


def _new_quarantine_name(parent_fd: int, name: str) -> str:
    for _ in range(8):
        candidate = f".{name}.quarantine-{secrets.token_hex(12)}"
        try:
            os.stat(candidate, dir_fd=parent_fd, follow_symlinks=False)
        except FileNotFoundError:
            return candidate
    raise ValueError("managed stale output quarantine allocation failed")


def _remove_stale_confined(
    root: pathlib.Path,
    relative: pathlib.Path,
    *,
    require_marker: bool,
    marker_predicate=None,
) -> None:
    with _open_confined_directory(root, relative.parent, create=False) as parent_fd:
        quarantine = _new_quarantine_name(parent_fd, relative.name)
        try:
            os.rename(
                relative.name,
                quarantine,
                src_dir_fd=parent_fd,
                dst_dir_fd=parent_fd,
            )
        except FileNotFoundError:
            return
        try:
            captured = _read_regular_fd(parent_fd, quarantine)
            if captured is None:
                raise ValueError("managed stale output capture disappeared")
            predicate = marker_predicate or _has_exact_generated_marker
            if require_marker and not predicate(captured):
                raise ValueError("managed stale output ownership changed")
        except (OSError, ValueError) as error:
            _restore_quarantine(parent_fd, quarantine, relative.name)
            raise ValueError("managed stale output is unsafe or unowned") from error
        os.unlink(quarantine, dir_fd=parent_fd)


def _remove_empty_owned_root(root: pathlib.Path, relative: pathlib.Path) -> None:
    try:
        with _open_confined_directory(root, relative.parent, create=False) as parent_fd:
            directory_flags = os.O_RDONLY | os.O_DIRECTORY | os.O_NOFOLLOW
            directory_flags |= getattr(os, "O_CLOEXEC", 0)
            try:
                owned_fd = os.open(relative.name, directory_flags, dir_fd=parent_fd)
            except FileNotFoundError:
                return
            try:
                if os.listdir(owned_fd):
                    raise ValueError("obsolete owned root contains unknown entries")
                identity = os.fstat(owned_fd)
                quarantine = _new_quarantine_name(parent_fd, relative.name)
                os.rename(
                    relative.name,
                    quarantine,
                    src_dir_fd=parent_fd,
                    dst_dir_fd=parent_fd,
                )
                try:
                    captured = os.stat(
                        quarantine, dir_fd=parent_fd, follow_symlinks=False
                    )
                    if (captured.st_dev, captured.st_ino) != (
                        identity.st_dev,
                        identity.st_ino,
                    ):
                        raise ValueError("obsolete owned root identity changed")
                    if os.listdir(owned_fd):
                        raise ValueError("obsolete owned root changed during removal")
                    os.rmdir(quarantine, dir_fd=parent_fd)
                except (OSError, ValueError) as error:
                    try:
                        os.stat(relative.name, dir_fd=parent_fd, follow_symlinks=False)
                    except FileNotFoundError:
                        os.rename(
                            quarantine,
                            relative.name,
                            src_dir_fd=parent_fd,
                            dst_dir_fd=parent_fd,
                        )
                    raise ValueError(
                        "obsolete owned root changed during removal"
                    ) from error
            finally:
                os.close(owned_fd)
    except FileNotFoundError:
        return


def write_projection(root: pathlib.Path, provider: str) -> None:
    expected = expected_projection(root, provider)
    owned_root = _owned_root(provider)
    _assert_confined_directory(root, owned_root)
    for relative in sorted(find_managed_stale_files(root, expected)):
        if not relative.is_relative_to(owned_root):
            continue
        is_legacy = relative.name == "skill.md"
        _remove_stale_confined(root, relative, require_marker=not is_legacy)
        if provider == "codex" and is_legacy:
            _remove_empty_owned_root(root, relative.parent)
    if provider == "codex":
        _remove_empty_owned_root(root, owned_root)
    for relative, content in sorted(expected.items()):
        _write_confined(root, relative, content)


def write_native_projection(root: pathlib.Path) -> None:
    root = root.absolute()
    expected = expected_native_projection(root)
    inventory = find_native_inventory_drift(root, expected)
    blocking = [finding for finding in inventory if finding.kind != "stale"]
    if blocking:
        raise ValueError("managed native projection contains unsafe or unowned entries")
    for finding in inventory:
        if finding.kind != "stale" or finding.path == pathlib.Path(".codex/skills"):
            continue
        legacy = _is_legacy_migration_path(finding.path)
        _remove_stale_confined(
            root,
            finding.path,
            require_marker=not legacy,
            marker_predicate=lambda content: (
                _has_exact_generated_marker(content)
                or _has_native_generated_marker(content)
            ),
        )
        if finding.path.parent not in NATIVE_MANAGED_ROOTS:
            _remove_empty_owned_root(root, finding.path.parent)
    for provider in MIGRATION_PROVIDERS:
        write_projection(root, provider)
    for owned_root in NATIVE_AGENT_ROOTS:
        _assert_confined_directory(root, owned_root)
    for relative in sorted(find_managed_stale_native_files(root, expected)):
        _remove_stale_confined(
            root,
            relative,
            require_marker=True,
            marker_predicate=_has_native_generated_marker,
        )
    for relative, content in sorted(expected.items()):
        _write_confined(
            root,
            relative,
            content,
            mode=0o755 if relative in NATIVE_EXECUTABLES else 0o644,
        )


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Render Stage 00 canonical shared skill projections."
    )
    mode = parser.add_mutually_exclusive_group(required=True)
    mode.add_argument("--check", action="store_true")
    mode.add_argument("--write", action="store_true")
    parser.add_argument("--root", type=pathlib.Path, default=ROOT)
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    args = parse_args(argv)
    root = args.root.absolute()
    try:
        if args.write:
            write_native_projection(root)
        findings = find_native_projection_drift(root)
    except (ContractLoadError, OSError, ValueError) as error:
        print(
            f"provider_surface_renderer: ERROR {type(error).__name__}", file=sys.stderr
        )
        return 1
    if findings:
        for finding in findings:
            print(
                f"projection drift: provider=native path={finding.path} kind={finding.kind}",
                file=sys.stderr,
            )
        return 1
    print("provider_surface_renderer: PASS providers=3 drift=0")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
